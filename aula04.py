# tipos int e float
# int > número inteiro
# o tipo int representa qualquer número inteiro
# positivo ou negativo. int sem sinal é considerado positivo

print(11) #int
print(-11) #int
print(0) #int

# float > número com ponto fluatuante
# representa qualquer número
# o tipo float representa qualquer número com ponto fluante
# positivo ou negativo. float sem sinal é considerado positivo

print(1.1, 12.39) #float

# a função type mostra para o python o tipo
# inferiu o valor

print(    type('hc')    )
print(    type(1)    )
print(    type(.1)    )  

