# Tipo de dados boolean (booleanos)
# Ao questionar algo em um programa,
# So exitem duas respostas possiveis
# True and False
# Existem varios operadores para questionar
# |Dentre eles o ==, que é um operador lógico que questiona
# se um valor é igual a outro|

print( 10 == 10) #True
print( 10 == 9) #False
print( type(10 == 9)) #False